// Switch Statement
let lightBulbStatus = "On";
switch(lightBulbStatus){
    case "On":
        lightBulbStatus = (`light buld is ${lightBulbStatus}`)
        break;
    case "Off":
        lightBulbStatus = (`light buld is ${lightBulbStatus}`)
        break;
    case "Broken":
        lightBulbStatus = (`light buld is ${lightBulbStatus}`)
        break;
    default:
        lightBulbStatus = 'Please choose the correct input (On/Off/Broken)'
}

console.log(lightBulbStatus)

